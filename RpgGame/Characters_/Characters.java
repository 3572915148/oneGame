package RpgGame.Characters_;

public class Characters {
    public String name;
    public int hp;
    public int blue;
    public int attack;
    public int defense;

    public Characters(String name, int hp, int blue, int attack, int defense) {
        this.name = name;
        this.hp = hp;
        this.blue = blue;
        this.attack = attack;
        this.defense = defense;
    }

}
